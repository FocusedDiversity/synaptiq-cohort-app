-- =============================================================================
-- 05_cohort_tables.sql
-- Cohort definitions, membership, and optional wide patient feature table.
-- Schema: dev.test_gold_ehr_cohort
--
-- Cohort classification axes (2 dimensions):
--
--   cohort_type     — WHY the cohort was built:
--     condition     - patients sharing a medical condition (T2DM, heart failure)
--     exposure      - patients grouped by intervention/drug/procedure
--     control       - patients WITHOUT the condition/exposure (comparator arm)
--     operational   - built for care management: high-cost, risk stratification,
--                     quality reporting, care gap identification
--     administrative - trial recruitment, population health reporting, billing
--
--   cohort_category — WHEN relative to the data:
--     retrospective - built from historical EHR data; outcomes already recorded
--     prospective   - defined on current criteria; captures new data over time
--
-- Cross-schema FK: cohort_member and patient_feature reference
-- dev.test_silver_ehr_clinical.patient (informational only, NOT ENFORCED).
--
-- VARIANT requires DBR 15.3+; replace qualifying_evidence with STRING (JSON)
-- on older runtimes.
-- Run after 04_nlp_tables.sql.
-- =============================================================================

CREATE TABLE IF NOT EXISTS dev.test_gold_ehr_cohort.cohort_definition (
  cohort_sk               BIGINT GENERATED BY DEFAULT AS IDENTITY,
  name                    STRING    NOT NULL,
  description             STRING,

  -- -----------------------------------------------------------------------
  -- Classification
  -- -----------------------------------------------------------------------
  cohort_type             STRING              COMMENT 'condition | exposure | control | operational | administrative',
  cohort_category         STRING              COMMENT 'retrospective | prospective',

  -- -----------------------------------------------------------------------
  -- Primary anchor concept (the clinical entity that defines entry)
  -- -----------------------------------------------------------------------
  primary_concept_code    STRING              COMMENT 'Primary code defining cohort entry: ICD-10-CM, RxNorm, CPT, HCC, custom',
  primary_concept_system  STRING              COMMENT 'ICD-10-CM | RxNorm | CPT | LOINC | HCC | custom',
  primary_concept_display STRING              COMMENT 'Human-readable name of the anchor concept',

  -- -----------------------------------------------------------------------
  -- Cohort logic
  -- -----------------------------------------------------------------------
  definition_logic        STRING              COMMENT 'SQL or structured JSON describing all inclusion / exclusion criteria',
  is_nlp_derived          BOOLEAN   DEFAULT false  COMMENT 'true if any criterion references note_nlp_entity',

  -- -----------------------------------------------------------------------
  -- Temporal parameters
  -- -----------------------------------------------------------------------
  index_event_description STRING              COMMENT 'What triggers cohort entry — e.g. "first T2DM diagnosis", "first metformin Rx", "top-5% spend in rolling 12 months"',
  lookback_days           INT                 COMMENT 'Retrospective lookback window in days; NULL = full available history',
  follow_up_days          INT                 COMMENT 'Prospective follow-up window in days; NULL = open-ended (ongoing)',

  -- -----------------------------------------------------------------------
  -- Common inclusion / exclusion filters
  -- -----------------------------------------------------------------------
  min_age_years           INT                 COMMENT 'Minimum patient age at index date (null = no lower bound)',
  max_age_years           INT                 COMMENT 'Maximum patient age at index date (null = no upper bound)',

  -- -----------------------------------------------------------------------
  -- Control cohort linkage
  -- -----------------------------------------------------------------------
  comparison_cohort_sk    BIGINT              COMMENT 'FK → cohort_definition: links a control cohort to its case cohort for RR / efficacy calculations',

  -- -----------------------------------------------------------------------
  -- Lifecycle / versioning (reproducibility for regulatory audits)
  -- -----------------------------------------------------------------------
  status                  STRING    DEFAULT 'draft'  COMMENT 'draft | active | archived',
  version                 INT       DEFAULT 1        COMMENT 'Version number — increment when definition_logic changes; old versions archived',
  created_by              STRING,
  created_at              TIMESTAMP DEFAULT current_timestamp(),

  CONSTRAINT pk_cohort_definition PRIMARY KEY (cohort_sk) NOT ENFORCED RELY
)
COMMENT 'Named patient cohort definitions. cohort_type classifies by purpose (condition/exposure/control/operational/administrative). cohort_category distinguishes retrospective from prospective study design. Linked to members via cohort_member.'
TBLPROPERTIES ('delta.feature.allowColumnDefaults' = 'supported');

