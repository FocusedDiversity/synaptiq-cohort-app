-- ---------------------------------------------------------------------------
-- cohort_member
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS dev.test_gold_ehr_cohort.cohort_member (
  cohort_member_sk    BIGINT GENERATED BY DEFAULT AS IDENTITY,
  cohort_sk           BIGINT    NOT NULL,
  patient_sk          BIGINT    NOT NULL,

  -- index_date: when the patient FIRST met the cohort entry criteria.
  -- Distinct from qualified_at (load time) — used for time-zero anchoring
  -- in retrospective studies and as the enrollment date in prospective ones.
  index_date          DATE                COMMENT 'Date patient first met cohort entry criteria (time-zero anchor)',

  qualified_at        TIMESTAMP,
  qualifying_source   STRING              COMMENT 'coded | nlp | both — how the patient qualified',
  qualifying_evidence VARIANT             COMMENT 'the condition / observation / note_nlp_entity rows that triggered inclusion (VARIANT; requires DBR 15.3+)',
  loaded_at           TIMESTAMP DEFAULT current_timestamp(),

  CONSTRAINT pk_cohort_member  PRIMARY KEY (cohort_member_sk) NOT ENFORCED RELY,
  CONSTRAINT fk_cm_cohort      FOREIGN KEY (cohort_sk)   REFERENCES dev.test_gold_ehr_cohort.cohort_definition   NOT ENFORCED RELY,
  CONSTRAINT fk_cm_patient     FOREIGN KEY (patient_sk)  REFERENCES dev.test_silver_ehr_clinical.patient         NOT ENFORCED RELY
)
COMMENT 'Cohort membership with explainable provenance — WHY each patient qualified (coded vs NLP), index_date for time-zero anchoring, and qualifying_evidence for audit.'
CLUSTER BY (cohort_sk)
TBLPROPERTIES ('delta.feature.allowColumnDefaults' = 'supported');

-- ---------------------------------------------------------------------------
-- patient_feature  (optional — wide table for downstream ML / risk scoring)
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS dev.test_gold_ehr_cohort.patient_feature (
  patient_sk           BIGINT    NOT NULL,
  age_years            INT,
  sex                  STRING,
  race                 STRING,
  ethnicity            STRING,
  encounter_count      INT,
  inpatient_count      INT,
  ed_count             INT,
  active_problem_count INT,
  distinct_med_count   INT,
  last_a1c             DOUBLE    COMMENT 'most recent HbA1c value (LOINC 4548-4)',
  last_bp_systolic     DOUBLE    COMMENT 'most recent systolic BP (mmHg)',
  last_bp_diastolic    DOUBLE    COMMENT 'most recent diastolic BP (mmHg)',
  last_bmi             DOUBLE,
  nlp_finding_count    INT       COMMENT 'distinct positive (non-negated, current) NLP-derived findings',
  feature_asof_date    DATE,
  loaded_at            TIMESTAMP DEFAULT current_timestamp(),
  CONSTRAINT pk_patient_feature  PRIMARY KEY (patient_sk) NOT ENFORCED RELY,
  CONSTRAINT fk_pf_patient       FOREIGN KEY (patient_sk) REFERENCES dev.test_silver_ehr_clinical.patient NOT ENFORCED RELY
)
COMMENT 'Wide per-patient feature table assembled from silver + nlp tables. One row per patient. Extend columns as needed for ML risk models (HCC, SDOH, utilization).'
CLUSTER BY (patient_sk)
TBLPROPERTIES ('delta.feature.allowColumnDefaults' = 'supported');
